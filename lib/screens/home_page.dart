Future<void> _autoGenerateMonthlyReportIfNeeded() async {
  final budgetSetting = await IsarService.getBudgetSetting();
  if (budgetSetting == null) return;
  if (budgetSetting.totalBudget <= 0) return;

  final cycleStartDay = budgetSetting.cycleStartDay.clamp(1, 28);
  // final now = DateTime.now();
  final now = getNow();
  final baseStart = now.day >= cycleStartDay
      ? DateTime(now.year, now.month, cycleStartDay)
      : DateTime(now.year, now.month - 1, cycleStartDay);

  final currentCycleStart = DateTime(
    baseStart.year,
    baseStart.month + _cycleOffsetMonths,
    baseStart.day,
  );

  try {
    await MonthlyReportService.autoGenerateIfNeeded(
      now: now,
      currentCycleStart: currentCycleStart,
      cycleStartDay: cycleStartDay,
    );
  } catch (e) {
    debugPrint('[MonthlyReport] auto generate skipped: $e');
  }
}

  Future<List<({String title, String message, String subMessage})>>
      _buildRoastCards({
    required List<Expense> currentCycleExpenses,
    required int totalBudget,
    required int usedAmount,
    required DateTime start,
    required DateTime endInclusive,
    required List<Map<String, dynamic>> dangerCategories,
  }) async {
    final latestCategoryTagUsedAmounts = <ExpenseJudgeTag, int>{};
    if (currentCycleExpenses.isNotEmpty) {
      final latestCategory = currentCycleExpenses.first.category;
      final categoryExpenses = currentCycleExpenses
          .where((expense) => expense.category == latestCategory)
          .toList();

      for (final expense in categoryExpenses) {
        final judge = ExpenseJudgeService.judge(
          expense: expense,
          totalBudget: totalBudget,
        );

        for (final tag in judge.tags) {
          latestCategoryTagUsedAmounts[tag] =
              (latestCategoryTagUsedAmounts[tag] ?? 0) + expense.amount;
        }
      }
    }

    final categoryCountMap = <String, int>{};
    final categoryAmountMap = <String, int>{};

    for (final expense in currentCycleExpenses) {
      final category = expense.category;
      categoryCountMap[category] = (categoryCountMap[category] ?? 0) + 1;
      categoryAmountMap[category] =
          (categoryAmountMap[category] ?? 0) + expense.amount;
    }

    final latestCategoryBudget = currentCycleExpenses.isEmpty
        ? 0
        : (() {
            final latestCategory = currentCycleExpenses.first.category;
            final matchedCategory = _budgetSetting?.categories
                .where((category) => category.name == latestCategory)
                .toList();
            if (matchedCategory == null || matchedCategory.isEmpty) return 0;
            return matchedCategory.first.budget;
          })();

    final latestCategoryUsed = currentCycleExpenses.isEmpty
        ? 0
        : (() {
            final latestCategory = currentCycleExpenses.first.category;
            return currentCycleExpenses
                .where((expense) => expense.category == latestCategory)
                .fold<int>(0, (sum, expense) => sum + expense.amount);
          })();

    final roastResult = await RoastService.build(
      totalBudget: totalBudget,
      usedAmount: usedAmount,
      expenses: currentCycleExpenses,
      dangerCategories: dangerCategories.take(3).toList(),
      latestCategoryBudget: latestCategoryBudget,
      latestCategoryUsed: latestCategoryUsed,
      cycleStart: start,
      cycleEnd: endInclusive,
      latestCategoryTagUsedAmounts: latestCategoryTagUsedAmounts,
    );

    final roastCards = <({String title, String message, String subMessage})>[];

    final subLines = roastResult.subMessage
        .split('\n')
        .map((text) => text.trim())
        .where((text) => text.isNotEmpty)
        .toList();

    final hasLayeredRoast = subLines.length >= 3;

    if (hasLayeredRoast) {
      final monthlySubMessage = subLines.isNotEmpty ? subLines.first : '';
      final latestMessage = subLines.length >= 2 ? subLines[1] : '';
      final latestSubMessage =
          subLines.length >= 3 ? subLines.sublist(2).join('\n') : '';

      roastCards.add((
        title: roastResult.title,
        message: roastResult.message,
        subMessage: monthlySubMessage,
      ));

      roastCards.add((
        title: '財布からもうひとこと',
        message: latestMessage,
        subMessage: latestSubMessage,
      ));
    } else {
      roastCards.add((
        title: roastResult.title,
        message: roastResult.message,
        subMessage: roastResult.subMessage,
      ));
    }

    return roastCards;
  }
