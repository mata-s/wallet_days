  int get _cycleStartDay {
    final day = _budgetSetting?.cycleStartDay ?? 1;
    return day.clamp(1, 28);
  }

({DateTime start, DateTime endExclusive}) _resolvedCurrentCycleRange() {
  if (_cycleOffsetMonths == 0 && _currentBudgetHistory != null) {
    return (
      start: _currentBudgetHistory!.startDate,
      endExclusive: _currentBudgetHistory!.endDate,
    );
  }

  final now = getNow();
  final startDay = _cycleStartDay.clamp(1, 28);

  final baseStart = now.day >= startDay
      ? DateTime(now.year, now.month, startDay)
      : DateTime(now.year, now.month - 1, startDay);

  final start = DateTime(
    baseStart.year,
    baseStart.month + _cycleOffsetMonths,
    baseStart.day,
  );
  final endExclusive = DateTime(start.year, start.month + 1, startDay);

  return (start: start, endExclusive: endExclusive);
}

DateTime get _currentCycleStart => _resolvedCurrentCycleRange().start;

DateTime get _currentCycleEndExclusive =>
    _resolvedCurrentCycleRange().endExclusive;

DateTime get _currentCycleEnd {
  final endExclusive = _currentCycleEndExclusive;
  return endExclusive.subtract(const Duration(days: 1));
}


  int get _totalBudget {
    return _budgetSetting?.totalBudget ?? 0;
  }


  int _remainingPeriodDays() {
  // final now = DateTime.now();
  final now = getNow();
  final today = DateTime(now.year, now.month, now.day);
  final end = DateTime(
    _currentCycleEnd.year,
    _currentCycleEnd.month,
    _currentCycleEnd.day,
  );

  final diff = end.difference(today).inDays;
  return diff < 0 ? 0 : diff;
}


  String _formatYen(int value) {
    final formatter = NumberFormat('#,###');
    return formatter.format(value);
  }

String _formatTimelineDate(DateTime date) {
  // final now = DateTime.now();
  final now = getNow();
  final today = DateTime(now.year, now.month, now.day);
  final target = DateTime(date.year, date.month, date.day);

  final diff = today.difference(target).inDays;

  if (diff == 0) return '今日';
  if (diff == 1) return '昨日';

  return DateFormat('M/d').format(date);
}

String _formatCyclePeriod() {
  final start = _currentCycleStart;
  final end = _currentCycleEnd;
  return '${start.month}/${start.day} 〜 ${end.month}/${end.day}';
}

