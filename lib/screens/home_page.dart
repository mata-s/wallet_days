Future<void> _loadInitialData() async {
  if (mounted) {
    setState(() {
      _isInitialLoading = true;
    });
  }

  await _syncBudgetHistoryIfNeeded();
  await _loadBudgetSetting();
  await _loadPremiumStatus();

  if (_isPremium && !_hasAutoGeneratedMonthlyReport) {
    await _autoGenerateMonthlyReportIfNeeded();
    _hasAutoGeneratedMonthlyReport = true;
  }

  await Future.wait([
    _loadExpenses(),
    _loadRankData(),
  ]);

  if (mounted) {
    setState(() {
      _isInitialLoading = false;
    });
  }
}

Future<void> _syncBudgetHistoryIfNeeded() async {
  final budgetSetting = await IsarService.getBudgetSetting();
  if (budgetSetting == null) return;
  if (budgetSetting.totalBudget <= 0) return;

  await BudgetHistoryService.syncIfNeeded(
    cycleStartDay: budgetSetting.cycleStartDay,
    totalBudget: budgetSetting.totalBudget,
  );

  _currentBudgetHistory = null;
}

  Future<void> _recomputeDerivedCaches() async {
    final start = _currentCycleStart;
    final endExclusive = _currentCycleEndExclusive;
    final endInclusive = _currentCycleEnd;

    final currentCycleExpenses = _expenses.where((expense) {
      final createdAt = expense.createdAt;
      return !createdAt.isBefore(start) && createdAt.isBefore(endExclusive);
    }).toList();

    final usedAmount = currentCycleExpenses.fold<int>(
      0,
      (sum, expense) => sum + expense.amount,
    );

    final totalBudget = _totalBudget;
    final remainingBudgetRaw = totalBudget - usedAmount;
    final remainingBudget = remainingBudgetRaw < 0 ? 0 : remainingBudgetRaw;

    int realWalletLifeDays = 0;
    if (_cycleOffsetMonths == 0 && totalBudget > 0 && remainingBudget > 0) {
      final totalCycleDays = endInclusive.difference(start).inDays;
      if (totalCycleDays > 0) {
        final plannedDailyBudget = totalBudget / totalCycleDays;
        if (plannedDailyBudget > 0) {
          realWalletLifeDays =
              (remainingBudget / plannedDailyBudget).ceil().clamp(1, 999);
        }
      }
    }

    int dailySpendingPaceYen = 0;
    if (_cycleOffsetMonths == 0 && usedAmount > 0) {
      // final now = DateTime.now();
      final now = getNow();
      final today = DateTime(now.year, now.month, now.day);
      final normalizedCycleStart = DateTime(start.year, start.month, start.day);
      final elapsedDays = today.difference(normalizedCycleStart).inDays + 1;
      if (elapsedDays > 0) {
        dailySpendingPaceYen = (usedAmount / elapsedDays).round();
      }
    }

    int plannedDailyBudgetYen = 0;
    if (_cycleOffsetMonths == 0 && totalBudget > 0) {
      final totalCycleDays = endInclusive.difference(start).inDays;
      if (totalCycleDays > 0) {
        plannedDailyBudgetYen = (totalBudget / totalCycleDays).round();
      }
    }

    final categories = _budgetSetting?.categories ?? [];
    final categorySummaries = categories.map((category) {
      final used = currentCycleExpenses
          .where((expense) => expense.category == category.name)
          .fold<int>(0, (sum, expense) => sum + expense.amount);
      final remaining = category.budget - used;
      final usageRate = category.budget == 0 ? 0.0 : used / category.budget;

      return {
        'name': category.name,
        'badge': category.badge,
        'budget': category.budget,
        'used': used,
        'remaining': remaining,
        'usageRate': usageRate,
      };
    }).toList();

    final dangerCategories = categorySummaries.where((category) {
      final budget = category['budget'] as int;
      if (budget == 0) return false;

      final usageRate = category['usageRate'] as double;
      final remaining = category['remaining'] as int;
      return remaining < 0 || usageRate >= 0.75;
    }).toList();

    dangerCategories.sort((a, b) {
      final aRate = a['usageRate'] as double;
      final bRate = b['usageRate'] as double;
      return bRate.compareTo(aRate);
    });

    final roastCards = await _buildRoastCards(
      currentCycleExpenses: currentCycleExpenses,
      totalBudget: totalBudget,
      usedAmount: usedAmount,
      start: start,
      endInclusive: endInclusive,
      dangerCategories: dangerCategories,
    );

    final remainingBudgetResult = RemainingBudgetService.build(
      totalBudget: totalBudget,
      usedAmount: usedAmount,
      cycleStart: start,
      cycleEnd: endInclusive,
    );

    if (!mounted) return;

    setState(() {
    _currentCycleExpensesCache = currentCycleExpenses;
    _usedAmountCache = usedAmount;
    _remainingBudgetCache = remainingBudget;
    _realWalletLifeDaysCache = realWalletLifeDays;
    _dailySpendingPaceYenCache = dailySpendingPaceYen;
    _plannedDailyBudgetYenCache = plannedDailyBudgetYen;
    _categorySummariesCache = categorySummaries;
    _dangerCategoriesCache = dangerCategories.take(3).toList();
    _roastCardsCache = roastCards;
    _remainingBudgetResultCache = remainingBudgetResult;
    });
  }

  Future<void> _loadExpenses() async {
    final expenses = await IsarService.getExpenses();
    if (!mounted) return;

    setState(() {
      _expenses = expenses;
    });

    await _recomputeDerivedCaches();
  }

  Future<void> _loadBudgetSetting() async {
    final budgetSetting = await IsarService.getBudgetSetting();
    if (budgetSetting == null) return;

    final histories = await IsarService.getBudgetHistories();
    final now = getNow();

    BudgetHistory? currentHistory;
    for (final history in histories) {
      if (!now.isBefore(history.startDate) && now.isBefore(history.endDate)) {
        currentHistory = history;
        break;
      }
    }

    if (!mounted) return;

    setState(() {
      _budgetSetting = budgetSetting;
      _currentBudgetHistory = currentHistory;
    });

    await _recomputeDerivedCaches();
  }

